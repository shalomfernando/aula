	public ArrayList<Pais> listarPais() {
		ArrayList<Pais> ps = new ArrayList<>();

		String sql = "select id, nome, populacao, area from pais";
		try (Connection conn = ConnectionFactory.obtemConexao(); PreparedStatement stm = conn.prepareStatement(sql);) {
			try (ResultSet rs = stm.executeQuery();) {
				while (rs.next()) {
					Pais pi = new Pais();
					pi.setId(rs.getInt("id"));
					pi.setNome(rs.getString("nome"));
					pi.setPopulacao(rs.getLong("populacao"));
					pi.setArea(rs.getDouble("area"));
					ps.add(pi);
				}
			} catch (SQLException e) {
				e.printStackTrace();
			}
		} catch (SQLException e1) {
			System.out.print(e1.getStackTrace());
		}
		return ps;
	}
